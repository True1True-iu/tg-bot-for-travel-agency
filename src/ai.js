async function getRedirectMessage(userMessage, currentQuestion) {
  const ai = getClient();
  if (!ai) {
    return fallback(currentQuestion);
  }

  try {
    const res = await ai.chat.completions.create({
      model: config.GROQ_MODEL,
      messages: [
        {
          role: "system",
          content: SYSTEM_PROMPT.replace("{question}", currentQuestion),
        },
        { role: "user", content: userMessage },
      ],
      max_tokens: 300,
      temperature: 0.3,
    });
    return res.choices[0].message.content;
  } catch (err) {
    const status = err?.status || err?.response?.status;
    const code = err?.code;
    const type = err?.type;
    const details =
      err?.error?.message ||
      err?.response?.data?.error?.message ||
      err?.message;
    console.error(
      `[ai] Groq request failed: status=${status || "n/a"} code=${code || "n/a"} type=${type || "n/a"} model=${config.GROQ_MODEL} details=${details}`,
    );
    return fallback(currentQuestion);
  }
}

function fallback(question) {
  return `Пожалуйста, ответьте на вопрос: ${question}`;
}

