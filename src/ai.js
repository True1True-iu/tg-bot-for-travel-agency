const OpenAI = require("openai");
const config = require("./config");

let client = null;

function getClient() {
  if (!client && config.GROQ_API_KEY) {
    client = new OpenAI({
      apiKey: config.GROQ_API_KEY,
      baseURL: "https://api.groq.com/openai/v1",
    });
  }
  return client;
}

